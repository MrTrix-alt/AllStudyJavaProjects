package learning.java.streamapi.upperbase.operations.collect;

public class Car
{
    private String name;
    private int price;

    public Car(String name, int price)
    {
        this.name=name;
        this.price=price;
    }

    public String getName()
    {
        return name;
    }

    public int getPrice()
    {
        return price;
    }
}
